package kz.pandev.bot.telegrambotapplication.enums;

/**
 * Перечисление состояний пользователя в процессе взаимодействия с ботом.
 * <p>
 * Используется для управления логикой диалогов, в частности,
 * чтобы определять, какой ввод ожидается от пользователя на текущем этапе.
 */
public enum UserState {

    /**
     * Стандартное состояние — пользователь не находится в процессе ввода данных.
     */
    NONE,

    /**
     * Ожидание ввода названия родительской (корневой) категории.
     */
    AWAITING_ROOT_CATEGORY_NAME,

    /**
     * Ожидание ввода названия дочерней категории.
     */
    AWAITING_CHILD_CATEGORY_NAME,

    /**
     * Ожидание подтверждения или выбора категории для удаления.
     */
    AWAITING_CATEGORY_DELETION
}
